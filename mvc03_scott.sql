--○ 실습 테이블 생성(TBL_MEMBERLIST)
CREATE TABLE TBL_MEMBERLIST
( MID           NUMBER
, NAME          VARCHAR2(30)
, TELEPHONE     VARCHAR2(50)
, CONSTRAINT MEMBERLIST_MID_PK  PRIMARY KEY(MID)
);

--○ 시퀀스 생성(MEMBERLISTSEQ)
CREATE SEQUENCE MEMBERLISTSEQ
NOCACHE;

--○ 샘플 데이터 입력
